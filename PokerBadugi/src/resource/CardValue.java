package resource;

/**
 *
 * @author lenovo
 */
public class CardValue {
    private String name;
    private int weight;
    private int order;
    
    public String getName() {
        return this.name;
    }
    
    public int getWeight() {
        return this.weight;
    }
    
    public int getOrder() {
        return this.order;
    }
    
    public void setName(String name) {
        this.name = name;
    }
    
    public void setWeight(int weight) {
        this.weight = weight;
    }
    
    public void setOrder(int order) {
        this.order = order;
    }
}
